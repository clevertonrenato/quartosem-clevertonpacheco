
package modelo;


public class Cadastro_lanche {
    private int id;
    private String lanche;
    private float precolanche;
    private String bebida;
    private float precobebida;

    public Cadastro_lanche() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getLanche() {
        return lanche;
    }

    public void setLanche(String lanche) {
        this.lanche = lanche;
    }

    public float getPrecolanche() {
        return precolanche;
    }

    public void setPrecolanche(float precolanche) {
        this.precolanche = precolanche;
    }

    public String getBebida() {
        return bebida;
    }

    public void setBebida(String bebida) {
        this.bebida = bebida;
    }

    public float getPrecobebida() {
        return precobebida;
    }

    public void setPrecobebida(float precobebida) {
        this.precobebida = precobebida;
    }

   
    
}
